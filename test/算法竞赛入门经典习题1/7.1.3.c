#include<stdio.h>
int main()
{
    int k,x,y;
    scanf("%d",&k);
    for (y = 1;y<=2*k;y++)
    {
        x = k*y/(y-k);
        printf("%d %d",x,y);
        if (1.0/k == 1.0/x+1.0/y) printf("1/%d =1/%d + 1/%d",k,x,y);
    }
    return 0;
}
